#include <iostream>

int main() {

    // Integer (whole number):
    int age = 14;
    int birthday = 2012;
    int height = 183;
    
    // Double (decimal number)
    double price = 12.99;
    double gpa = 4.0;
    double temperature = 23.5;

    // Char (stores a single character)
    char grade = 'A';
    char initial = 'P';
    char currency = '$';
    
    // Boolean (true or false)
    bool tall = true;
    bool strong = false;
    bool forSale = true;

    // String (sequence of text)
    std::string name = "John Doe";
    std::string food = "Chicken";
    std::string item = "Keyboard";

    std::cout << "Hello " << name << '\n';
    std::cout << "I am " << age << " years old.";
    return 0;
}
